gallbladder.&nbsp;In most cases, it is performed using&nbsp;<b>laparoscropic
techniques&nbsp;</b>(with special instruments under the control of a video
camera through a small incision).&nbsp;The operation is performed in cases of
constantly repeated attacks of biliary colic, with the development of
complicated forms of acute cholecystitis, as well as with calcification of the
gallbladder wall.&nbsp;After cholecystectomy, complications and relapses of the
disease are extremely rare.&nbsp;Open access surgery is rarely performed when
individual anatomical features do not allow the operation with a
laparoscope.&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
