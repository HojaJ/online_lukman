is recorded in 10-15% of the population of Russia.&nbsp;Women are twice as
likely to get sick, the average age of the onset of the disease is 40-69
years.&nbsp;In elderly people over 65, gallstones are found in 30% of cases.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
