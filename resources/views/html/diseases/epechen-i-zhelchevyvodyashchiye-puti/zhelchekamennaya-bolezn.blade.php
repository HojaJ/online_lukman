font-family:"Times New Roman",serif;color:black'>lowers the concentration of
cholesterol in bile and prevents excessive absorption of cholesterol in the
intestine.&nbsp;As a result, cholesterol is excreted from the stones and
excreted in the bile.&nbsp;Bilirubin stones are not treated with this
substance.&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
