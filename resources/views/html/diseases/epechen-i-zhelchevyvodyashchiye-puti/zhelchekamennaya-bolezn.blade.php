lithotripsy&nbsp;</span></b><span style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>.&nbsp;A special apparatus generates high frequency waves and
focuses them on the stones in the gallbladder.&nbsp;As a result, the stones are
shattered into small pieces.&nbsp;The method can be used if the stone is not
more than 3 cm in diameter.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
