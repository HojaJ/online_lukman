font-family:"Times New Roman",serif;color:black'>During the recovery period,
diet must be followed.&nbsp;The diet should contain a sufficient amount of
easily digestible protein, as well as a limited carbohydrate content
(especially in obese people).&nbsp;Food should be gentle and thermally
processed.&nbsp;You should avoid eating cold food (drinks from the
refrigerator, ice cream, etc.), which can provoke an attack of biliary colic.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
