style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>is an
operation through a puncture in the abdominal wall and a small incision in the
gallbladder.&nbsp;Through the incision, using hardware techniques, the
destruction and removal of calculi are performed.&nbsp;After the operation,
there is a risk of relapse of the disease.&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
