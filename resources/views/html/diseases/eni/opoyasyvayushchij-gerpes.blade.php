<p style='margin-top:0in;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:24.0pt;color:black'>Herpes zoster</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>What is herpes zoster</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>Herpes zoster is an acute
infectious disease that results from reactivation of the varicella-zoster virus
and is characterized by inflammation of the skin and nerve tissue.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>The disease is also known as</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>Shingles;</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>herpes zoster.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>In English, the disease is
called&nbsp;<i>Herpes Zoster&nbsp;</i>.&nbsp;</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>Causes</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>The causative agent of herpes
zoster is human herpesvirus type 3 (&nbsp;<i>Varicella Zoster&nbsp;</i>virus&nbsp;,&nbsp;<i>Human
herpesvirus&nbsp;</i>, HHV-3,&nbsp;<i>Varicella-zoster&nbsp;virus,</i>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;VZV),
which also causes chickenpox.&nbsp;Chickenpox occurs after primary infection
and causes a generalized rash, while herpes zoster occurs after reactivation of
a previous infection, with a tendency to spread along a single nerve.&nbsp;Only
persons who have previously had chickenpox in a typical or latent form can get
sick with herpes zoster.&nbsp;The virus spreads by airborne
droplets.&nbsp;After a person has had chickenpox, a certain amount of the virus
remains in his body, persisting in the area of &#8203;&#8203;the nerve ganglia
(nodes).&nbsp;Under the influence of various unfavorable factors that reduce
immunity, the virus awakens and causes inflammation in its localization and
associated skin areas.&nbsp;Relapses of the disease occur in less than 5% of
those who have recovered.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>Who is at risk</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>Elderly people (60
years and older);</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>patients with cancer
and HIV infection;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>children who have had
chickenpox before the age of 1 year;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>patients with chronic
diseases (rheumatoid arthritis, inflammatory bowel disease, chronic obstructive
pulmonary disease, bronchial asthma, chronic kidney disease, diabetes mellitus,
hyperparathyroidism);</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>patients after
radiation and chemotherapy, long-term treatment with systemic hormonal drugs,
organ transplants;</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>people under prolonged
stress.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>How often does it occur</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>The incidence of herpes zoster
in various countries is 0.4-1.6 cases per 1000 people per year in people under
the age of 20 and 4.5-11.8 cases per 1000 people per year in older age
groups.&nbsp;Several studies have shown that people with fair skin are much
more likely to develop shingles than dark-skinned people.&nbsp;The incidence of
herpes zoster is higher among women than among men, increases with age and is
not seasonal.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>Symptoms</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>The incubation period for
herpes zoster (from the transfer of chickenpox to the activation of the virus)
lasts for many years.&nbsp;The disease can begin with&nbsp;<b>fever&nbsp;</b>,&nbsp;<b>lack
of appetite,&nbsp;</b>and&nbsp;<b>fatigue&nbsp;</b>.&nbsp;During the prodromal
period of the disease,&nbsp;<b>pain&nbsp;</b>and&nbsp;<b>sensory
disturbances&nbsp;</b>appear&nbsp;in the area of &#8203;&#8203;the affected
skin area (dermatome).&nbsp;After the onset of prodromal symptoms, there are:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>spotty redness of
the skin,&nbsp;</span></b><span style='font-size:13.5pt;color:black'>sometimes
accompanied by induration in the area of &#8203;&#8203;the affected area;&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>regional&nbsp;<b>enlargement
and soreness of the lymph nodes&nbsp;</b>;&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>grouped&nbsp;<b>vesicles&nbsp;</b>,
which continue to appear for 3-4 days, develop on a red, inflamed base on one
side of the body and are limited to the area of &#8203;&#8203;innervation of
one nerve (often the area of &#8203;&#8203;innervation of the trigeminal nerve,
as well as the skin of the trunk of the T3-L2 segments).&nbsp;</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>Skin lesions in the chest area
are observed in more than 50% of cases.&nbsp;The bubbles are initially clear,
but then they become cloudy, rupture, covered with a crust, which usually
disappears by the end of the 3-4th week of the disease.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>Pain syndrome associated with
herpes zoster has&nbsp;<i>acute&nbsp;</i>,&nbsp;<i>subacute&nbsp;</i>and&nbsp;<i>chronic&nbsp;</i>phases.&nbsp;The
acute phase occurs in the prodromal period and lasts for 30 days, the subacute
phase - no more than 120 days.&nbsp;Pain syndrome lasting more than 120 days is
defined as&nbsp;<b>postherpetic neuralgia&nbsp;</b>that can last for months or
years, causing physical distress and significantly reducing the quality of life
of patients.&nbsp;Pain syndrome, as a rule, is accompanied by sleep
disturbances, loss of appetite and weight loss, chronic fatigue, depression,
which leads to social maladjustment of patients.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>Herpes zoster in children is
not as severe as in the elderly, with less severity of pain;&nbsp;postherpetic
neuralgia is also rare.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>A number of patients may
experience an atypical course of herpes zoster, manifested either by the
absence of bubbles (&nbsp;<i>abortive form&nbsp;</i>), or by the appearance of
bubbles in the form of bubbles (&nbsp;<i>bullous form&nbsp;</i>), with bloody
contents (&nbsp;<i>hemorrhagic form&nbsp;</i>) or with the formation of a dark
scab after opening them (&nbsp;<i>gangrenous, necrotic form&nbsp;</i>), as well
as the spread of vesicles outside the affected area (&nbsp;<i>disseminated
form&nbsp;</i>).</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>According to the clinical
course, the disease is classified into:</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><i><span style='font-size:13.5pt;color:black'>ganglionic
forms&nbsp;</span></i><span style='font-size:13.5pt;color:black'>;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>herpes zoster with
lesions of the&nbsp;<i>mucous membranes&nbsp;</i>;&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><i><span style='font-size:13.5pt;color:black'>ear and
eye&nbsp;(ophthalmic herpes) forms&nbsp;</span></i><span style='font-size:13.5pt;
color:black'>;&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;color:black'>herpes zoster with
damage to the&nbsp;<i>autonomic ganglia&nbsp;</i>;&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><i><span style='font-size:13.5pt;color:black'>meningoencephalitic
form&nbsp;</span></i><span style='font-size:13.5pt;color:black'>.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>Diagnosis of the disease</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:13.5pt;color:black'>Questioning and inspection</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>A thorough survey of the
patient is carried out about the nature of the onset of the disease, pain
syndrome and rashes.&nbsp;When interviewing, attention is drawn to the presence
of a history of chickenpox.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>When examining a patient, the
doctor examines the skin and visible mucous membranes, palpation (probing) of
the lymph nodes, auscultation (listening) of the lungs, evaluates the
neurological symptoms, as well as the prevalence and distribution of the
elements of the rash.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:13.5pt;color:black'>Laboratory examination</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>A microscopic
diagnostic method&nbsp;</span></b><span style='font-size:13.5pt;color:black'>,
namely, the detection of Aragao bodies (accumulations of the virus) in smears
of vesicle fluid, the Tzank test (superficial scraping from the base of a recently
ruptured vesicle with Wright-Giemsa staining), which allows detecting
multinucleated giant cells in herpes and&nbsp;<i>varicella-zoster&nbsp;</i>infections,
as well as determination of the increased content of cells or protein in the
cerebrospinal fluid.&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>The immunofluorescent
diagnostic method&nbsp;</span></b><span style='font-size:13.5pt;color:black'>is
used to detect the antigens of the virus in smears-prints from the contents of
the vesicles.&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>Molecular
biological diagnostic method (PCR)&nbsp;</span></b><span style='font-size:13.5pt;
color:black'>is used for the genetic identification of the&nbsp;<i>Varianella
Zoster&nbsp;</i>virus&nbsp;.&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>The virological
diagnostic method&nbsp;</span></b><span style='font-size:13.5pt;color:black'>makes
it possible to isolate the virus on embryonic human cell cultures.&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>The
immunocytochemical diagnostic method&nbsp;</span></b><span style='font-size:
13.5pt;color:black'>is used to detect the antigen of the virus in the blood and
cerebrospinal fluid.&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>Serological
diagnostic method&nbsp;(ELISA)&nbsp;</span></b><span style='font-size:13.5pt;
color:black'>to confirm the diagnosis of herpes zoster (increase in the titer
of specific antibodies by 4 or more times).&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>Clinical blood
test&nbsp;</span></b><span style='font-size:13.5pt;color:black'>, in which
there is a decrease in the number of leukocytes, an increase in the number of
lymphocytes and monocytes, normal ESR.</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>A general urine
test&nbsp;</span></b><span style='font-size:13.5pt;color:black'>and a&nbsp;<b>biochemical
blood test are&nbsp;</b>performed to assess the severity and complicated course
of herpes zoster.&nbsp;&nbsp;&nbsp;</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:13.5pt;color:black'>Functional, radiation and
instrumental diagnostics</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>X-ray of the chest
organs is&nbsp;</span></b><span style='font-size:13.5pt;color:black'>carried
out during differential diagnosis in the prodromal period, when there are no
vesicular rashes, but there are symptoms of intoxication, fever, pain syndrome
with pulmonary infarction, pleurisy, as well as with suspected pneumonia.&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>Electrocardiogram&nbsp;</span></b><span
style='font-size:13.5pt;color:black'>and&nbsp;<b>echocardiography&nbsp;</b>are
indicated in patients with herpes zoster if myocarditis is
suspected.&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>An ultrasound
examination of the abdominal organs is&nbsp;</span></b><span style='font-size:
13.5pt;color:black'>performed in patients with herpes zoster, symptoms of
intoxication, fever, pain syndrome with cholecystitis, pancreatitis, renal
colic, acute appendicitis.&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>Magnetic resonance
imaging&nbsp;</span></b><span style='font-size:13.5pt;color:black'>and&nbsp;<b>computed
tomography&nbsp;</b>are used for violations of the function of internal organs
in patients with herpes zoster to clarify the localization of the pathological
process and the structure of the focus.&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span style='font-size:13.5pt;color:black'>Neurosonography&nbsp;</span></b><span
style='font-size:13.5pt;color:black'>and&nbsp;<b>electroencephalography
are&nbsp;</b>used in patients with focal neurological symptoms, seizures, signs
of intracranial hypertension.&nbsp;&nbsp;&nbsp;</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>Treatment</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:13.5pt;color:black'>Treatment goals</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
