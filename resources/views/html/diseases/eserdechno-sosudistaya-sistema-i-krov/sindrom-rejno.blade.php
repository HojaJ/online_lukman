<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>Raynaud's phenomenon.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
font-family:"Times New Roman",serif;color:black'>Causes</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Raynaud's syndrome is a
disease with many causes that develops as a result of a violation of hormonal,
nervous or intravascular regulation of the processes of expansion (vasodilation)
and narrowing (vasoconstriction) of blood vessels.&nbsp;In Raynaud's syndrome,
the processes of formation of vascular regulators, such as nitric oxide,
thromboxane, angiotensin, serotonin, are also disrupted.&nbsp;Violation of the
concentration of these mediators leads to active vasoconstriction in response
to external stimuli (cold, emotional stress).</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Also, in the processes of
regulation of vascular tone, calcitonin-gene-related peptide plays an important
role, the violation of the synthesis of which leads to insufficient
vasodilation.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>The causes of secondary
Raynaud's syndrome are diseases that cause structural changes in the vascular
wall and affect the mechanisms of nervous and humoral regulation of vascular
tone.</span></p>

