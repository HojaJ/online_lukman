<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The exact reasons for
the development of colon neoplasms are currently unknown.&nbsp;It is assumed
that viruses, hereditary predisposition, negatively affecting the intestines
and the body as a whole, environmental factors can play a role in this.&nbsp;A
number of genetic markers associated with a high risk of developing colon
cancer have been identified, and risk factors associated with diet and
lifestyle have been identified for this type of neoplasm.&nbsp;For benign
neoplasms, such connections have not yet been established.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
