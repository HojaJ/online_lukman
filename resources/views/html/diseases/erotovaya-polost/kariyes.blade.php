14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>With timely detected
caries, the prognosis is favorable.&nbsp;Modern methods of treatment allow you
