<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>What
is renal colic</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Renal colic is sudden,
intense pain that occurs in the lumbar region as a result of impaired urine
flow.&nbsp;It can occur in any part of the urinary system.&nbsp;The organs of
the urinary system include two kidneys, two ureters, the bladder and the
urethra.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>The
disease is also known as</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>In English, this
pathology is called renal colic.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>Causes</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The most common cause
of renal colic is a stuck stone in the urinary tract.&nbsp;This usually occurs
in the ureter, where the stone stretches the walls as it tries to travel
downward.&nbsp;In the place where the stone gets stuck, inflammation occurs,
the flow of urine is disturbed, stones with sharp edges can injure the mucous
membrane and cause bleeding.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Kidney stones are
formed due to a violation of mineral metabolism, which leads to the deposition
of salts of various minerals in the renal pelvis (this is a cavity in the
kidney that passes into the ureter).&nbsp;Salts gradually accumulate and turn
into stony formations.&nbsp;Most often these are salts of calcium, uric acid
and phosphate.&nbsp;The sizes of stones vary from grains of sand to several
centimeters.</span></p>

