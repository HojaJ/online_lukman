rejection of alcohol, drinking plenty of fluids, at least 2 liters of fluid per
day.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>A hot bath or heating
pad in the suprapubic area can relieve symptoms of cystitis.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>With interstitial
cystitis, Kegel exercises are indicated to strengthen the muscles of the pelvic
floor.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Medications</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span lang=EN-US style='font-size:10.0pt;font-family:
Symbol;color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span lang=EN-US style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>Antibacterial drugs.&nbsp;If the patient is diagnosed with acute
bacterial cystitis for the first time, then empirical antibiotic therapy is
prescribed, that is, without identifying the pathogen, since in this case the
pathogen will be Escherichia coli or other gram-negative
microorganisms.&nbsp;The duration of antibiotic therapy for uncomplicated
cystitis is from 1 to 7 days, depending on the drug, which is selected taking
into account the age, concomitant conditions, the presence of diabetes
mellitus.&nbsp;Therapy is carried out on an outpatient basis. In severe
cystitis, blood in the urine, fever, the patient is shown hospitalization and
intravenous administration of antibacterial drugs.</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span lang=EN-US style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
