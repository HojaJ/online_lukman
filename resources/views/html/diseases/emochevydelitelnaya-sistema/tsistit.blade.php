</span></span><span lang=EN-US style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>Antihistamines.&nbsp;One of the mechanisms for the development of
interstitial cystitis may be an allergic reaction.&nbsp;</span><span
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Antihistamines
help reduce inflammation.</span></p>

