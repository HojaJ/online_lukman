<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>With perennial allergic
conjunctivitis, symptoms can appear in waves, but often there is a slight
burning and itching in the eyes, as well as an increased sensitivity to
light.&nbsp;If the allergic process affects other parts of the eye, then, for
example,&nbsp;<b>atopic keratoconjunctivitis&nbsp;</b>may develop&nbsp;(in
addition to the conjunctiva, the cornea is also involved),&nbsp;<b>atopic
cataract&nbsp;</b>(lens), etc.&nbsp;&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>There is always a connection
between the onset of symptoms and exposure to an allergen (contact with an
