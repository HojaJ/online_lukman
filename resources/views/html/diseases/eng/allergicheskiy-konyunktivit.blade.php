household detergents or perfumes, etc.), but it cannot always be clearly
distinguished, especially when there are several allergens.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Symptoms of the disease may be
present continuously with an episodic worsening of the condition, or appear
only upon contact with a causal trigger.&nbsp;Most often, allergic
conjunctivitis is combined with&nbsp;<b>allergic rhinitis&nbsp;</b>(runny
nose), less often with other&nbsp;<b>allergic diseases&nbsp;</b>(for example,
bronchial asthma).&nbsp;&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>A characteristic sign of
pollen sensitization is a deterioration when going outside, in dry windy
weather, while in a suburban area and other places where flowering is more
active.&nbsp;At the same time, as a rule, after rain there is an improvement.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>With fungal sensitization,
exacerbation often occurs in spring and autumn (in central Russia), but in
conditions of high humidity, symptoms can disturb the entire warm period of the
year.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Clinical manifestations of
hypersensitivity to tick-borne allergens can be&nbsp;<b>seasonal&nbsp;</b>(spring
and autumn are the period of active reproduction of ticks) or be&nbsp;<b>year-round&nbsp;</b>.&nbsp;If
there is a simultaneous sensitization to allergens that occur daily, and to
allergens present in the air only in a certain season, then the manifestations
are year-round in nature with a seasonal deterioration in the
condition.&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>In some cases, allergic
conjunctivitis can develop in response to certain medications or substances
that get in the eyes (for example, contact lens solution, eye drops, makeup).</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
