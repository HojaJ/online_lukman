<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Home treatment</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>In most cases, treatment takes
place on an outpatient basis.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>First of all,&nbsp;<b>it is
important to exclude contact with the allergen&nbsp;</b>.&nbsp;In situations
where this is not possible, it is necessary to minimize its impact (for
example, during the flowering period, it is imperative to change at home in
home clothes, close windows, use an air purifier and humidifier).&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Everyone who suffers from
allergies should&nbsp;<b>adhere to a hypoallergenic living environment&nbsp;</b>,
even if they do not have sensitization specifically to household
allergens.&nbsp;Bloom allergy patients should not use herbal preparations and
cosmetics.&nbsp;It is also necessary to&nbsp;<b>follow a diet&nbsp;</b>taking
into account possible cross-sensitivities between foods and causally
significant pollen or fungal allergens.&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Drug treatment for&nbsp;</span></b><span
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>allergic
conjunctivitis consists of therapy to relieve symptoms during exacerbations and
basic treatment to prevent exacerbations.&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>In addition, in the presence
of sensitization to certain allergens (for example, pollen, household dust), it
is possible to carry out treatment aimed at eliminating the cause of the
development of the disease.&nbsp;This type of treatment includes ASIT (antigen
specific immunotherapy).&nbsp;As a rule, several courses are carried out, as a
result of which the hypersensitivity to the causative allergen disappears or
significantly weakens.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Drugs</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Prophylactic treatment begins
2-3 weeks before the expected exacerbation and includes the appointment of
antihistamines of local or systemic action, cromoglycic acid preparations,
