<p class=MsoNormal style='margin-bottom:14.0pt;line-height:normal'><b><span
style='font-size:24.0pt;font-family:"Times New Roman",serif;color:black'>Allergic
conjunctivitis</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
font-family:"Times New Roman",serif;color:black'>What is allergic
conjunctivitis</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Allergic conjunctivitis&nbsp;</span></b><span
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>is
inflammation of the conjunctiva (the thin, transparent membrane that covers the
outside of the eyes and the back of the eyelids) caused by an allergy.&nbsp;The
disease can occur all year round or be seasonal.&nbsp;The seasonal form occurs
when sensitization (hypersensitivity) to pollen (hay fever) and fungal
allergens, and year-round - to house dust mite allergens, library dust,
epidermal allergens of animals and birds, insect allergens, mold allergens,
food products and drugs with constant their reception, professional
allergens.&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
