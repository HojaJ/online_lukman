<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>After a traumatic brain
injury, headache occurs in 50-70% of cases, in 35-45% of the pain becomes
chronic.&nbsp;Every fourth patient experiences a headache for 5 years or more.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>Symptoms</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Headache after TBI is
nonspecific.&nbsp;In most cases, it is pressing or encircling like a
&quot;hoop&quot;, more often observed in the frontal lobes, but can also be
localized in the parietal, temporal and occipital regions.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>With an increase in
intracranial pressure, pain occurs in the early morning hours, when the
production of cerebrospinal fluid is maximum.&nbsp;The pain has a bursting
character, it can intensify with a change in weather, a sharp change in body
position, sometimes accompanied by nausea, less often vomiting.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>PTHB can also present
with migraine-like or cluster-like pains.&nbsp;Patients complain of
high-intensity throbbing pain in one half of the head.&nbsp;The pain can have
precursors (aura), intensify during physical activity, be accompanied by
nausea, vomiting, photophobia and sound phobia.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>In some patients, pain
can spread along the fibers of the trigeminal (temple, eye, upper and lower
jaw) and occipital nerves (occipital region).</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>PTHB occurs more often
after mild TBI, much less often after severe TBI.</span></p>

