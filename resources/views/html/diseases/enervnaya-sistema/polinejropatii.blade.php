<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
font-family:"Times New Roman",serif;color:black'>How often does it occur</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The prevalence of the
disease is 2400 cases per 100 thousand (2.4%).&nbsp;The most common causes of
polyneuropathy are type 2 diabetes mellitus (12-95% of patients with diabetes)
and chronic alcoholism (12-30% of patients).</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>Symptoms</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>How the disease will
manifest itself depends on the type of affected nerve fibers.&nbsp;If sensitive
nerve fibers are involved, symptoms such as&nbsp;<b>numbness, burning,
goosebumps, burn-like pain, electric shock, imbalance, up to falls&nbsp;</b>will
occur&nbsp;<b>.</b>&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>There are also variants
of the disease, affecting mainly the motor fibers.&nbsp;They will manifest
as&nbsp;<b>muscle weakness, atrophy, seizures, fasciculations&nbsp;</b>-
involuntary muscle twitching.&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>If the fibers that
innervate the internal organs are damaged, this is accompanied by dry skin, a
change in its color, hair loss on it, diarrhea, dysfunction of the bladder
(increased urination or difficulty urinating), palpitations and other symptoms.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>Diagnosis
of the disease</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Questioning
and inspection</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>When&nbsp;<b>questioned,
the&nbsp;</b>doctor finds out what complaints the patient is concerned
about.&nbsp;In addition, in order to diagnose this pathology, it is necessary
to know whether the patient has diseases that most often lead to the onset of
polyneuropathy.&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>After that, the doctor
proceeds to the&nbsp;<b>examination&nbsp;</b>- assesses the color of the skin,
their temperature and humidity.&nbsp;With the help of a special instrument
-&nbsp;<b>monofilament, it&nbsp;</b>conducts a sensitivity study.&nbsp;Instead,
the doctor can use a pendulum and assess at what moment after the beginning of
its oscillation, the patient stops feeling it.&nbsp;&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>To assess&nbsp;<b>reflexes,
the&nbsp;</b>doctor strikes with a neurological hammer at special points -
under the patella, Achilles tendon, ulnar fossa, above the olecranon, zygomatic
arch.&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Laboratory
examination</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>With polyneuropathy, a
laboratory test is performed to find out the reason why it has arisen.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span lang=EN-US style='font-size:10.0pt;font-family:
Symbol;color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span lang=EN-US style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>Determine the level of&nbsp;<b>glucose, glycated hemoglobin.&nbsp;</b>The
