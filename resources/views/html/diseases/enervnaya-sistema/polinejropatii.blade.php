13.5pt;font-family:"Times New Roman",serif;color:black'>The consequences of
diabetic polyneuropathy are the appearance of ulcers of the lower extremities,
the development of a diabetic foot, which in the absence of further treatment
can lead to amputation.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
