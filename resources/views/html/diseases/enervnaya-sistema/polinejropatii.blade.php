<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The main measures for
the prevention of polyneuropathy for healthy people are diet and alcohol
control.&nbsp;It is necessary to get a sufficient amount of vitamin B12 with
food or in the form of tablets (important for vegetarians).&nbsp;The daily
intake of vitamin B12 is 2.4 mcg per day.&nbsp;It is also important to comply
with measures to prevent infection with infectious diseases: HIV, influenza,
enterovirus and others.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
