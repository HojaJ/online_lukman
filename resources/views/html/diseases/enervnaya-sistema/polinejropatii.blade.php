<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The rehabilitation
program depends on which area of &#8203;&#8203;the body and which nerves are
affected by the disease.&nbsp;Most often includes exercises that improve
stability while standing and walking, as well as develop balance, coordination
and body awareness.&nbsp;In the case of paresis, the goal is to increase muscle
strength and function and maintain or restore muscle balance to prevent
deformities and contractures.&nbsp;If hand function is impaired, occupational
therapy is indicated, which, if necessary, is supplemented by the use of
appropriate assistive devices.&nbsp;Light physical activity is
desirable.&nbsp;An ergometer and strength training three times a week have a
positive effect on fitness and muscle strength.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
