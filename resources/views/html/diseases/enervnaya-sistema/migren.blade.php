<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>It is believed that
migraine occurs against the background of a hereditary predisposition.&nbsp;In
patients prone to migraine, there is an increased sensitivity of the fibers of
the trigeminal nerve, high excitability of the cerebral cortex in combination
with low activity of the antinoseptive system (the brain system that inhibits
the conduction of pain impulses).&nbsp;During a pain attack, the activation of
nerve fibers occurs, the perivascular endings of which secrete pain
neuropeptides: calcitonin-gene-binding peptide, neurokinin A and substance P.
...</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
