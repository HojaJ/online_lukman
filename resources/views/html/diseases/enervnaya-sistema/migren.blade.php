<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>During the period of an
attack, it is recommended to avoid strong stimuli that can aggravate the pain
syndrome (loud sounds, bright light, physical activity).&nbsp;It is advisable
to use earplugs, sleep masks.&nbsp;If possible, lie down in a dark room with a
sufficient supply of fresh air.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
