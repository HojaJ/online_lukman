color:black'>Triptans are 5HT1-serotonin receptor agonists.&nbsp;They are used
to relieve a painful attack that has already begun.&nbsp;The drugs activate the
anti-pain mechanisms of the brain stem, narrow the dilated vessels and reduce
the excitability of the trigeminal system, thereby stopping the attack.</span></p>

