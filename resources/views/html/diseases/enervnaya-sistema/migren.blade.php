<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>How
often does it occur</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The frequency of
migraine in the population is 12-20% in women and 2-10% in men.&nbsp;The
average age of the onset of the disease is 20-35 years.&nbsp;The frequency and intensity
of seizures reaches its maximum by the age of 35-40, after which it fades,
completely stopping after 65 years.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The chance of a
migraine in a child if one of the parents suffered from pain is 60%, both
parents are more than 90%.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>In 2/3 of cases, migraine
without aura (simple migraine) is recorded.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>Symptoms</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>A migraine is
characterized by severe throbbing pain in one side of the head.&nbsp;The pain
is more often localized in the frontal and temporal regions and the eye region,
less often in the parietal and occipital regions, increases during physical
activity, lasts 6-18 hours, in some cases up to 2-3 days.&nbsp;Sleep usually
relieves migraine symptoms.&nbsp;After an attack, weakness, drowsiness, and
frequent and profuse urination are observed.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>A migraine attack is
often accompanied by nausea, vomiting, intolerance to loud sounds, bright
light.&nbsp;On the side of the pain syndrome, vegetative symptoms may be
present: redness of the eye, watery eyes, nasal congestion.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Migraine can have
precursors - “migraine with aura”.&nbsp;The aura occurs 3-4 hours before the
attack and is represented by neurological symptoms: loss of visual fields
(hemianapsia), the appearance of bright spots and flies before the eyes
(photopsia), sensitivity disorders (numbness and tingling of the fingertips,
face).</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>More rare types of
migraines are also distinguished: ophthalmoplegic migraine (pain is combined
with transient strabismus, dilated pupil, drooping of the eyelid), hemiplegic
migraine (the migraine aura is represented by a violation of the motor function
of one half of the trunk), an aura without migraine (after a typical aura, a
pain attack does not develop).</span></p>

