<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Emotional stress leads
to increased tone of the pericranial muscles (muscles of the head and
neck).&nbsp;Excessive muscle tone irritates and activates superficial pain
receptors, resulting in sensitization (increased pain sensitivity).&nbsp;As a
result, previously not felt stimuli begin to be perceived by the patient as
painful.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Prolonged tonic muscle
spasm leads to a deterioration in blood supply and the release of inflammatory
mediators, which further aggravates the pain syndrome.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Prolonged uncontrolled
intake of painkillers can lead to the formation of a chronic form of HDN.</span></p>

