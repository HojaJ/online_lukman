Europe ranges from 0.9% to 2.5%.&nbsp;In the United States, the disease has
been diagnosed in 6 million men and 2 million women.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Gout affects men 7 times more
