<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>An important factor for the
doctor is the intensity of pain and the ability to perform normal movements,
since acute myalgia can have similar symptoms with a muscle strain or tear,
however, with these pathologies, the pain will be sharper, more intense and the
ability to perform routine movements will be impaired.&nbsp;Another condition
with which the doctor conducts differential diagnosis is rhabdomyolysis, a
destruction of skeletal muscles that can occur with intense training in people
with a hereditary predisposition to this condition or at high ambient
temperatures.&nbsp;Mild rhabdomyolysis will have a similar clinical picture and
be accompanied by muscle edema.&nbsp;Severe rhabdomyolysis due to massive
muscle breakdown can lead to multiple organ failure.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
