to send weakened signals to the muscles at the moment when it realizes that
further muscle work will harm the body.&nbsp;According to this theory, delayed
muscle pain may be the result of brain activity, which thus prevents repeated
exercise in the first days after exercise to allow the body to recover.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
