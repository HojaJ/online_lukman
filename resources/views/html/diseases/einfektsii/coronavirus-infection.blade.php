<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Most often, epidemics
of coronavirus infection are caused by a coronavirus, which was originally
zoonotic, but then transmitted from an animal to a person.&nbsp;The reason for
the spread of infections is currently the overpopulation of cities, an increase
in meat consumption, the expansion of residential areas to areas of habitat of
wild animals and affordable transport that allows the sick to move across
countries and continents.</span></p>

