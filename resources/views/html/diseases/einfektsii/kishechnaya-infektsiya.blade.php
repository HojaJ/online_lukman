<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:13.5pt;color:black'>Recovery and improvement of
the quality of life</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>Timely and adequate treatment
for almost all intestinal infections leads to a complete restoration of the
quality of life.&nbsp;After suffering an illness, it is useful for some time to
follow the principles of gentle nutrition, gradually introduce new foods into
the diet.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>Possible complications</span></b></p>

