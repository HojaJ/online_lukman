</span></span><b><span style='font-size:13.5pt;color:black'>Severe dehydration
is&nbsp;</span></b><span style='font-size:13.5pt;color:black'>characterized by
fluid loss of more than 10% of a person's weight.&nbsp;This is a
life-threatening condition that requires urgent medical attention.&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
