</span></span><b><span style='font-size:13.5pt;color:black'>endoscopic
examination of the intestine&nbsp;</span></b><span style='font-size:13.5pt;
color:black'>- to assess the state of the mucous membrane and biopsy;&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
