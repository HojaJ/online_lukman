<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>How often does it occur</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>The incidence of influenza is
characterized by annual epidemic rises with a pronounced seasonal
character.&nbsp;In the countries of the Northern Hemisphere, influenza
epidemics usually occur in winter.&nbsp;Most influenza-related deaths in
industrialized countries occur in people 65 years of age or older.&nbsp;In the
period 2009 - 2014.&nbsp;the average annual incidence of influenza in various
cities ranged from 19.2 to 30 per 100 people.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>The incidence of influenza in
children is higher than in the adult population.&nbsp;According to the World
Health Organization, every year during outbreaks of influenza in the world, up
to 15% of the population falls ill, 250-500 thousand of them die.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>Symptoms</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>The incubation period for
influenza ranges from a few hours to 7 days.&nbsp;The onset of the disease is
acute.&nbsp;From the first hours of the disease, patients have symptoms
of&nbsp;<b>intoxication&nbsp;</b>(chills, headache, dizziness, pain when moving
the eyes, increasing weakness, pain in joints and muscles), an&nbsp;<b>increase
in&nbsp;</b>body&nbsp;<b>temperature&nbsp;</b>up to 38.5-40.0 ° C.&nbsp;May
appear&nbsp;<b>epistaxis&nbsp;</b>(sometimes abundant and continuous).&nbsp;The
duration of the febrile period in uncomplicated influenza does not exceed 5
days.&nbsp;In older people, the flu can go on without fever.&nbsp;<b>Catarrhal
syndrome&nbsp;</b>joins in a few hours / day and is manifested by congestion,
rawness behind the breastbone, dry cough and runny nose.&nbsp;The flu is
usually benign in 3-7 days, although coughing and general malaise can persist
for more than two weeks.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:18.0pt;color:black'>Diagnosis of the disease</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span style='font-size:13.5pt;color:black'>Questioning and inspection</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>A thorough survey of the
patient is carried out about the nature of the onset of the disease,
intoxication and catarrhal syndromes, the intake of antiviral drugs, and
concomitant chronic diseases.&nbsp;When interviewing, attention is drawn to the
presence of persons with influenza infection in the patient's environment, as
well as those who have been vaccinated against influenza.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>During the examination, a
general examination of the patient, temperature measurement, examination of the
skin and visible mucous membranes, palpation (probing) of the lymph nodes,
auscultation (listening) of the lungs and heart, and the severity of
intoxication and fever are assessed.</span></p>

