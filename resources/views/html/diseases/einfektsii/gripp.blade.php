<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span style='font-size:13.5pt;color:black'>During the first 48 hours of
the development of influenza,&nbsp;<i>antiviral drugs&nbsp;</i>are&nbsp;used
for treatment&nbsp;, which have a suppressive effect on the multiplication of
the virus.&nbsp;Most patients with influenza require&nbsp;<i>symptomatic
therapy&nbsp;</i>, which includes&nbsp;<i>non-steroidal anti-inflammatory
drugs&nbsp;</i>,&nbsp;<i>mucolytic and expectorant drugs&nbsp;</i>,&nbsp;<i>antihistamines,&nbsp;</i>and&nbsp;<i>vasoconstrictors&nbsp;</i>.&nbsp;With
the development of severe forms, antiviral drugs are prescribed from the moment
the patient is admitted to the hospital, regardless of the day of
illness.&nbsp;With a severe course of influenza, complicated by an
infectious-toxic shock,&nbsp;<i>glucocorticosteroids&nbsp;</i>are
prescribed&nbsp;in complex therapy along with&nbsp;<i>detoxification
agents&nbsp;</i>,&nbsp;<i>oxygen&nbsp;</i>therapy is&nbsp;carried out&nbsp;<i>.&nbsp;</i>With
the addition of a bacterial infection and the development of complications of
influenza, the appointment of&nbsp;<i>antibiotics is&nbsp;</i>indicated&nbsp;.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span></p>

