</span></span><span style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>Puncture of the cyst is carried out both for diagnostic purposes
and for therapeutic purposes.&nbsp;However, after puncture and removal of the
contents of the cyst, in most cases, after a while, it fills up again.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
