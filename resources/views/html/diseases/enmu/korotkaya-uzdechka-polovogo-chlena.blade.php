13.5pt;font-family:"Times New Roman",serif;color:black'>A short frenulum of the
penis is found in 96% of newborn boys, but as their body develops, the foreskin
acquires the necessary mobility and the problem disappears on its own.&nbsp;At
the age of three, the frequency of detection of pathology is 10%, in adult
uncircumcised men - 5%.</span></p>

