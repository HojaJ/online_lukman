<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Congenital hydrocele is
found in newborn boys.&nbsp;The mechanism of its formation is associated with
the natural process of the formation of the reproductive system.&nbsp;At the
3rd month of intrauterine development, the peritoneal process protrudes into
the inguinal canal in the form of a diverticulum - a blind-closing
canal.&nbsp;At the 7th month, the testicles reach the inguinal ring and
gradually descend to the bottom of the scrotum towards the end of
pregnancy.&nbsp;After they have gone down, under the action of sex hormones,
the process of overgrowing of the peritoneal diverticulum is started.&nbsp;In
this case, the testicle shell is formed from its distal part.&nbsp;By the time
of birth, in 80-85% of newborns, the diverticulum is completely closed and
represents a vaginal ligament.</span></p>

