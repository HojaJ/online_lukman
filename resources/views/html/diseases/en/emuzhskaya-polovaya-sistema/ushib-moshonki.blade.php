<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Procedures</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Puncture
of the scrotal hematoma.&nbsp;</span></b><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>With severe bruises,
the integrity of the vessels may be violated, the blood flows into the scrotal
cavity.&nbsp;Small hematomas can resolve on their own, large ones require
medical intervention, the size of the hematoma is established according to
ultrasound data.&nbsp;The indication for puncture of a hematoma is an increase
in its volume, size more than 5 cm.&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Surgery</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Surgical treatment is
used in cases of complicated bruises of the scrotum, which are accompanied by
rupture of the testicle and hematocele - an accumulation of blood between the
membranes of the testicle.&nbsp;If the size of the hematocele is three times
the size of a healthy testicle, this is an indication for emergency surgery.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
