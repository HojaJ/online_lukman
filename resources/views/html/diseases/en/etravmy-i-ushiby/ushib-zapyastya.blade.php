<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The doctor needs to
find out about the circumstances of the injury, the prescription of the
incident, the sensations directly during the injury and complaints at the time
of the examination.&nbsp;He will assess the severity of edema, hematoma, the
ability to move the brush.&nbsp;With a bruise, clenching into a fist,
rotational movements in the wrist joint may be difficult due to swelling and
pain, but still preserved.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
