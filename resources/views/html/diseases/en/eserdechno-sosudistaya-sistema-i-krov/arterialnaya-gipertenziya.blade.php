<p class=MsoNormal style='margin-bottom:14.0pt;line-height:normal'><b><span
lang=EN-US style='font-size:24.0pt;font-family:"Times New Roman",serif;
color:black'>Arterial hypertension</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>What
is arterial hypertension</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Arterial hypertension
is a stable increase in blood pressure (BP) from 140/90 mm
Hg.&nbsp;Art.&nbsp;and higher.&nbsp;A large number of diseases can lead to a
constant increase in blood pressure, if such a disease can be established,
arterial hypertension is called secondary, but if one reason cannot be
identified, arterial hypertension is called essential, or primary.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>A prolonged increase in
pressure to high numbers has a very negative effect on the body.&nbsp;First of
all, the so-called target organs, which include the heart, kidneys, brain and
retina, are affected.&nbsp;Their performance deteriorates, which can lead to
dangerous outcomes such as strokes and heart attacks.&nbsp;This is why it is
very important to monitor your blood pressure and keep it within the normal
range.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>The
disease is also known as</span></b></p>

