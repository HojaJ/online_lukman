<p class=MsoNormal style='margin-bottom:14.0pt;line-height:normal'><b><span
lang=EN-US style='font-size:24.0pt;font-family:"Times New Roman",serif;
color:black'>Pulmonary embolism (PE)</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>What
is pulmonary embolism</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Pulmonary embolism is a
blockage by a thrombus or embolus of the trunk or branch of the pulmonary
artery.&nbsp;At the same time, vascular resistance in the vessels of the lungs
increases, followed by disruption of the heart, leading in most cases to its
arrest.&nbsp;Also, specific substances are released, the action of which leads
to a drop in blood pressure and shock.&nbsp;If a small branch of the pulmonary
artery is clogged, it causes infarction pneumonia - an inflammation in the lung
tissue that develops as a result of a violation of blood supply.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
