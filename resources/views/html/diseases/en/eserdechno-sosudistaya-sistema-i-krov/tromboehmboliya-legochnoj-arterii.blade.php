</span></span><b><span lang=EN-US style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>X-ray&nbsp;</span></b><span lang=EN-US style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>.&nbsp;The x-ray also reveals
nonspecific changes, such as vasodilation of the lungs, darkening in the lung
tissue (infarction pneumonia), high standing of the dome of the diaphragm.</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
