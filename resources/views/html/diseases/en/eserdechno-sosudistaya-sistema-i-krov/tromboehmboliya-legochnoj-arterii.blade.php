revealed.&nbsp;During auscultation, the doctor determines an increased heart
rate, characteristic murmurs in the heart and chest.&nbsp;The likelihood of PE
in a patient is also assessed, based on whether he was affected by risk
factors, for example, an examination is carried out for signs of thrombophlebitis
of the lower extremities.&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span lang=EN-US style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span lang=EN-US style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>ECG&nbsp;</span></b><span lang=EN-US style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>.&nbsp;On the ECG, changes are
determined that are not specific for PE, but indirectly indicate an increase in
pressure in the right ventricle, which makes it possible to outline a further
plan of measures for making a diagnosis.</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
