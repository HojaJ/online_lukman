style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><b><span lang=EN-US style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>Laboratory diagnostics.&nbsp;</span></b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>In
a&nbsp;<b>clinical analysis of blood&nbsp;</b>, signs of inflammation&nbsp;<b>are&nbsp;</b>determined:
an increase in the number of leukocytes to 10,000 (with a heart
attack-pneumonia, the value is higher), an increase in ESR.&nbsp;An indicator
indicating the process of thrombus formation is&nbsp;<b>D-dimer&nbsp;</b>, a
decay product of coagulation factors that are synthesized in the body in PE in
large quantities.&nbsp;An increase in the level of atrial sodium uretic
peptide, an indicator of a decrease in heart function, is
determined.&nbsp;Also, in patients with pulmonary embolism observed increase in
myocardial markers -&nbsp;<b>troponin I and T&nbsp;</b>.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
