<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>imbalance.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
font-family:"Times New Roman",serif;color:black'>Diagnosis of the disease</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Questioning and inspection</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>During the interview,
the doctor asks the patient whether there are complaints.&nbsp;Next, an&nbsp;<b>otoscopy&nbsp;</b>is&nbsp;performed&nbsp;-
examination of the external auditory canal and the tympanic membrane, revealing
its thinning, sometimes it may look pink.&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Laboratory
examination</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The results of
laboratory tests are of high diagnostic value for detecting genetically
determined otosclerosis.&nbsp;For this, a&nbsp;<b>sequencing&nbsp;</b>method is
used&nbsp;.&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>In all other cases,
laboratory tests are not used.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Functional,
radiation and instrumental diagnostics</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>To detect hearing
loss,&nbsp;<b>tests&nbsp;</b>are performed&nbsp;<b>with a tuning fork&nbsp;</b>(Rinne
and Federici).&nbsp;After hitting the tuning fork, it is alternately brought to
the mastoid process or tragus, then to the external auditory canal and it is
noted when in one area or another the patient stops hearing the ringing emitted
by the tuning fork.&nbsp;Thus, the process of conducting sound through air and
through bone in a patient is compared.&nbsp;Normally, sound continues to travel
through air longer than through bone.&nbsp;Using this method, conductive
(impaired mobility of the auditory ossicles or membrane) and sensorineural
(pathology of the auditory nerve or receptor apparatus) hearing loss are
differentiated.&nbsp;Weber's experiment is also performed: the doctor puts a
tuning fork on the crown of the head, and depending on the type of hearing
loss, there will be a difference in the perception of sound by the patient and
the healthy ear.&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Tonal
threshold audiometry is&nbsp;</span></b><span lang=EN-US style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>also performed&nbsp;.&nbsp;The
patient is put on headphones, which send signals of varying volume, and he
presses a button if he hears a signal.&nbsp;Thus, it is determined what the
level of hearing loss is.&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>For diagnostics, the
method of&nbsp;<b>acoustic impedance measurement is also used&nbsp;</b>.&nbsp;Using
a special device, they determine the degree of mobility of the eardrum,
auditory ossicles, and contraction of the intra-aural muscles in response to
stimulation.&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>To obtain objective
data on the presence of osteosclerosis,&nbsp;<b>CT of the temporal bone is
used&nbsp;</b>.&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
