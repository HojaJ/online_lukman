13.5pt;font-family:"Times New Roman",serif;color:black'>- drugs that increase
the calcium content in bones, prevent bone resorption and stabilize its
structure;&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span lang=EN-US style='font-size:10.0pt;font-family:
