font-family:"Times New Roman",serif;color:black'>Mechanism of occurrence</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>The patient initially has a
narrowing of the upper airway.&nbsp;The cause may be diseases of the ENT
organs, for example, curvature of the nasal septum or enlargement of the
tonsils or dysfunction of the pharyngeal muscles due to endocrine diseases,
alcohol intake, age-related changes.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>During sleep, the muscles of
the pharynx relax and the walls of the pharynx become more mobile.&nbsp;At the
moment of the next inhalation, the patency of the upper airways is disturbed,
which leads to a stop of ventilation in the lungs.&nbsp;In this case, the
respiratory muscles begin to work more actively.&nbsp;At the same time, a lack
of oxygen develops and the level of carbon dioxide in the blood
rises.&nbsp;This stimulates the respiratory center in the brain, making sleep
more shallow.&nbsp;After the restoration of breathing, the person again plunges
into deep sleep and the whole cycle repeats.</span></p>

