inhalation is&nbsp;</span></b><span lang=EN-US style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>performed if triptans are
contraindicated.&nbsp;Through the mask, the patient is supplied with 100%
oxygen at a rate of 7-10 liters per minute.&nbsp;The duration of the procedure
is 15 minutes.&nbsp;Oxygen inhalation has no contraindications and does not
cause side effects.&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Surgical
operations</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
