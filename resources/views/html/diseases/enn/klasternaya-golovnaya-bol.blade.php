<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Cluster headache is a
rare disease that affects 0.5% of the population.&nbsp;Men suffer 2-4 times
more often than women.&nbsp;The first painful attack occurs at the age of 20-45
years.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>5% of patients suffer
from a chronic form of cluster pain, in 25% of cases only one attack develops
during their life.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
font-family:"Times New Roman",serif;color:black'>Symptoms</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span lang=EN-US style='font-size:10.0pt;font-family:
Symbol;color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span lang=EN-US style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>Extremely intense, always one-sided, excruciating pain.&nbsp;Pain
attacks occur in series that last from several weeks to 1-2 months.&nbsp;During
a series of attacks, pain can occur daily or every few days, at the same
time.&nbsp;After this, a stable remission occurs, which can last for several
years.&nbsp;Pain often occurs at night and in the early morning.&nbsp;It can
have a pulsating, burning or cutting character with spread to the lower jaw,
neck, ear.&nbsp;The attack begins suddenly and quickly (after 10-15) minutes
reaches its maximum intensity.&nbsp;The duration of cluster pain is from 15
minutes to 3 hours, after which the painful attack subsides spontaneously.</span></p>

<p class=MsoNormal style='margin-left:64.9pt;text-indent:-.25in;line-height:
normal'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
