</span></span><span lang=EN-US style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>Vegetative manifestations - simultaneously with pain, one or more
of the following manifestations occur: ear congestion, watery eyes, redness of
the eye and one half of the face, nasal congestion, swelling and drooping of
the eyelids, sweating and burning in the face, constriction of the pupil.&nbsp;</span><span
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Symptoms
occur on the same side as the headache.</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span lang=EN-US style='font-size:10.0pt;font-family:
