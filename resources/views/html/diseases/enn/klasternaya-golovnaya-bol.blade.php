<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>Causes</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The causes of cluster
headache are not fully understood.&nbsp;It is assumed that the basis of the onset
of pain syndrome is a violation in the work of the circadian rhythm - the daily
biological clock of the body.&nbsp;In this case, the hypothalamus probably
secretes pain peptides (calcitonin-gene-binding peptide, substance
P).&nbsp;This, in turn, leads to the development of aseptic inflammation, edema
and vasodilation.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Impaired histamine
metabolism is also involved in the formation of cluster headache
attacks.&nbsp;Histamine is involved in vasodilation (expansion) of blood
vessels.&nbsp;A pathologically dilated vessel mechanically irritates the
branches of the trigeminal nerve, causing an attack of acute pain.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
