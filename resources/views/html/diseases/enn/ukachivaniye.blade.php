<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>What
is motion sickness</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Motion sickness is a
complex of symptoms that occur in a person as a result of monotonous
fluctuations, mainly when traveling in transport.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>The
disease is also known as</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>Seasickness;</span></p>

<p class=MsoNormal style='margin-bottom:14.0pt;margin-left:64.9pt;text-indent:
-.25in;line-height:normal'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><span style='font-size:13.5pt;font-family:"Times New Roman",serif;
color:black'>motion sickness.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>In English, this
pathology is called motion sickness.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:18.0pt;font-family:"Times New Roman",serif;color:black'>Causes</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Motion sickness is
usually caused by low-frequency lateral and vertical movement (for example,
when moving in air, sea or road transport) or occurs when driving in a virtual
simulator (video games, virtual simulators).</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Motion sickness
develops due to a discrepancy between real and expected sensory data.&nbsp;To
date, the exact mechanism of motion sickness is unknown.&nbsp;The theory of
sensory conflict and neural disparity is the most widely accepted theory to
explain motion sickness.&nbsp;This theory describes the conflict between the
visual, vestibular and somatosensory systems resulting from real or virtual
movement.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Traffic information
mismatch can occur in two situations.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>In the first case, this
is a discrepancy between the data that comes through the visual analyzer and
from the vestibular apparatus.&nbsp;A good example is a person who travels in a
passenger seat in a car or in a ship's cabin.&nbsp;The vestibular system
perceives acceleration during movement, and the visual system transmits data on
the relative immobility of surrounding objects.&nbsp;Sensory mismatch occurs,
resulting in nausea, dizziness, and other symptoms.&nbsp;The more pronounced
the mismatch, the brighter the symptoms will be.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>In the second case,
conflicting information comes from the semicircular canals and otolith organs
located in the inner ear and forming part of the vestibular apparatus.&nbsp;The
more often the receptors are irritated, the less pronounced the symptoms of
motion sickness.&nbsp;Training for pilots, sailors, astronauts is based on
this.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Anyone can develop
motion sickness, but children under the age of two rarely suffer from it due to
inadequate spatial orientation using the visual analyzer.&nbsp;Elderly people,
on the other hand, are less likely to experience motion sickness
symptoms.&nbsp;Staying in a stuffy, poorly ventilated room increases the
manifestation of motion sickness.</span></p>

