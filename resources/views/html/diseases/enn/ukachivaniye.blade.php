<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Alcohol and smoking should
be avoided the day before and on the day of travel.&nbsp;It is not recommended
to eat spicy, fatty foods.&nbsp;During the trip, eat small meals and drink
plenty of water.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Unpleasant sounds and
odors should be avoided.&nbsp;Also, you should not discuss with other passengers
your tendency to motion sickness and focus on unpleasant symptoms.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>It is recommended to be
located in the middle of the vehicle, in a comfortable position with your eyes
closed and listening to pleasant music, to minimize head movements and not to
look out of the window at a rapidly changing landscape.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>It makes sense to
practice breathing exercises before traveling.</span></p>

