<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
font-family:"Times New Roman",serif;color:black'>Diagnosis of the disease</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Questioning and inspection</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>When&nbsp;<b>interviewed,
the&nbsp;</b>doctor asks whether the patient wears contact lenses, how often
they change them, whether there has been any discomfort associated with wearing
the lenses recently, whether the symptoms were preceded by trauma or swimming
in water.&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>During the&nbsp;<b>examination,&nbsp;</b>characteristic
clinical signs of keratitis are revealed: corneal opacity, vascular
proliferation, edema and redness of the conjunctiva, constriction of the pupil,
the presence of inflammatory discharge in the anterior chamber of the eye, as
well as photophobia, eyelid spasm, lacrimation and soreness, decreased
sensitivity of the cornea.&nbsp;The longer the inflammatory process lasts, the
deeper the layers of the cornea it captures, in the end erosion is formed, and
then the ulcer of the stratum corneum.&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>Laboratory examination</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>To diagnose infectious
keratitis, a&nbsp;<b>smear is&nbsp;</b>taken&nbsp;<b>from the surface of the
cornea&nbsp;</b>and&nbsp;<b>sowing of the discharge from the eyes to determine
the pathogen&nbsp;</b>.&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>If viral keratitis is
suspected, the doctor will prescribe&nbsp;<b>PCR diagnostics of discharge from
the eyes, lacrimal fluid&nbsp;</b>and&nbsp;<b>a blood test to determine IgG and
IgM antibodies to herpes simplex virus type 1 and 2&nbsp;</b>.&nbsp;To diagnose
a fungal infection, scrapings from ulcers are taken under local anesthesia or
discharge from the eye for microscopic, mycological examination and PCR
diagnostics.&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:13.5pt;
