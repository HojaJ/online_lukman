<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span style='font-size:13.5pt;
font-family:"Times New Roman",serif;color:black'>At the first symptoms of
keratitis, you should stop wearing contact lenses.&nbsp;It is recommended to
drink at least 1.5 liters of fluid per day to help the body cope with
inflammation and replenish tears.&nbsp;You should, if possible, completely stop
or limit computer work, give your eyes as much rest as possible.&nbsp;Sunglasses
are shown to protect the eyes from ultraviolet radiation.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
