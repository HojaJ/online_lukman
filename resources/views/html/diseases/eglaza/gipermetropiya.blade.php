found in 75-85% of newborns and infants in the first year of life, from 5 years
of age the percentage of hyperopia among children decreases sharply.&nbsp;After
40 years, a new rise in the detection of hyperopia begins; in people over 65,
hyperopia occurs in 93-97%.&nbsp;At the same time, hyperopia is more common in
rural residents than in urban ones.</span></p>

