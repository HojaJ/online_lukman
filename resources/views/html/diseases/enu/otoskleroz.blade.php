sulfur with cotton swabs, hairpins, matches and other objects.&nbsp;To maintain
hygiene, it is enough to rinse the auricle, lobes and the area behind the ears
with water.&nbsp;It is not necessary to clean out the wax, as it has a
protective function, preventing dirt and bacteria from entering the ear canal.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
