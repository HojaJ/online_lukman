<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>ENT prescribes research
methods to assess the state of the auditory analyzer: tone threshold
audiometry, suprathreshold audiological tests, hearing examination in an
extended range of sound frequencies, tympanometry, acoustic reflexometry.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
