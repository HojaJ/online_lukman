<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Treatment of
mastoiditis should be carried out in an otorhinolaryngological hospital.&nbsp;Patients
are recommended bed rest, drinking plenty of fluids.&nbsp;During treatment and
after - during the rehabilitation period - intensive loads on the hearing organ
are not recommended: listening to loud music, especially with headphones,
working in a production facility with a high noise level, attending concerts
and public events with a high noise level.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Medications</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Early administration of
