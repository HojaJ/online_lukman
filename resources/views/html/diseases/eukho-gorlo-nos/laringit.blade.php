recommended.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Symptomatic therapy is
reduced to the appointment of analgesic, antipyretic
drugs.&nbsp;Vasoconstrictor nasal drops and oral antihistamines may be given to
relieve mucosal edema and better drainage of the middle ear.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
style='font-size:13.5pt;font-family:"Times New Roman",serif;color:black'>Surgery</span></b></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The main surgical
method for the treatment of mastoiditis is antrotomy.&nbsp;This is the opening
of the mastoid process and the removal of pus from its cavity (during the
formation of an abscess).&nbsp;Absolute indications for antrotomy: symptoms of
meningitis, sepsis, development of a subperiosteal abscess.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
