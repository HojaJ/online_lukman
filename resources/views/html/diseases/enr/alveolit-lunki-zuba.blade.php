<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>After tooth extraction,
a blood clot normally forms, the function of which is to protect the wound from
the ingress of microorganisms contained in the oral cavity and capable of
causing inflammation.&nbsp;If the disintegration of a blood clot increases (for
example, when taking certain medications) or it is washed out (for example,
when rinsing), the tooth socket is exposed and becomes vulnerable to pathogenic
and opportunistic microflora of the oral cavity.&nbsp;Further, as a result of
exposure to bacterial toxins, a local inflammatory reaction occurs.</span></p>

