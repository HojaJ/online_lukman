<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Alveolitis is the most
common complication after tooth extraction: on average, after tooth extraction,
it develops in 3-5% of cases, with complex extractions with massive tissue
damage (for example, when removing &quot;wisdom teeth&quot;) - in 40% or
more.&nbsp;In addition, the likelihood of developing inflammation of the tooth
socket increases with the age of the patient.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
