<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Symptoms usually
develop 1-3 days after tooth extraction and persist for 7-10 days.&nbsp;The
main symptom of alveolitis is pain.&nbsp;The pain syndrome is localized at the
site of removal, is acute and pulsating in nature.&nbsp;Over time, pain can
spread to the upper and lower jaw, temples, eyes and ear.&nbsp;Often, patients
notice a specific putrid odor from the mouth and an unpleasant taste in the
mouth.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
