<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Fracture of the distal
ends of the radius and ulna is a violation of the integrity of the bones of the
lower part of the forearm near the hand.&nbsp;Typical fracture site.&nbsp;The
distal, that is, the far or lower end of the radius and ulna, together with the
wrist bones form the wrist joint.&nbsp;A fracture of the distal ends of the
radius and ulna is often referred to as a&nbsp;<b>fracture of the wrist&nbsp;</b>for
short&nbsp;.&nbsp;</span></p>

