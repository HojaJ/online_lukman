<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>The sprains of
the&nbsp;<b>posterior cruciate ligament&nbsp;</b>are mainly due to direct
impact on the front of the knee.&nbsp;For example, hitting your knee on the
dashboard in a car accident or hitting your bent knee hard while playing
sports.&nbsp;Injuries of this ligament are common among football players,
basketball players, volleyball players.&nbsp;<b>The medial collateral
ligament&nbsp;</b>stabilizes the knee joint and prevents inward displacement of
the lower leg.&nbsp;Most often, the stretching of this ligament occurs due to
the bending of the lower leg outward, which is typical for skiers and football
players.&nbsp;<b>Lateral collateral ligament.&nbsp;</b>Supports the outside of
the knee.&nbsp;Stretching of this ligament is the least likely to occur because
a blow must be delivered to the inside of the knee to damage
it.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
