<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Dizziness is a
condition in which a person feels either an imaginary rotation of the body
around space, or the rotation of the surrounding space around his own
body.&nbsp;Also, dizziness is understood as a state of lightheadedness, blurred
vision, weakness in the legs and other symptoms.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
