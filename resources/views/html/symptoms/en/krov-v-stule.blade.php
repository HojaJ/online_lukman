0in'><b><span lang=EN-US style='font-size:24.0pt;color:black'>Blood in the
stool</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span lang=EN-US style='font-size:18.0pt;color:black'>A brief
description of the symptom</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span lang=EN-US style='font-size:13.5pt;color:black'>Blood in the stool
is a symptom that appears as a result of a violation of the integrity of the
mucous membrane or vessels of the gastrointestinal tract (GIT) and the
development of bleeding.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span lang=EN-US style='font-size:18.0pt;color:black'>Do I need
emergency help</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span lang=EN-US style='font-size:13.5pt;color:black'>Emergency care may
be required for massive (profuse) bleeding.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><b><span lang=EN-US style='font-size:18.0pt;color:black'>Classification</span></b></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span lang=EN-US style='font-size:13.5pt;color:black'>Blood in the stool
results from bleeding from the upper gastrointestinal tract (esophagus,
stomach, duodenum) or from the lower gastrointestinal tract (small and large
intestine).</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
0in'><span lang=EN-US style='font-size:13.5pt;color:black'>Depending on the
level of the pathological process, blood streaks and the stool itself may have
a different color.&nbsp;Dark stools (black, tarry) - melena - a consequence of
bleeding from the upper gastrointestinal tract.&nbsp;The lower along the digestive
tract the source of bleeding, the brighter the streaks of blood will be, up to
invariable scarlet drops.</span></p>

<p style='margin-top:14.0pt;margin-right:0in;margin-bottom:14.0pt;margin-left:
