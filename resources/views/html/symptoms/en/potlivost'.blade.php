<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Sweating
(hyperhidrosis) is a condition of excessive sweating, in more quantities than
is necessary to maintain a normal body temperature.&nbsp;Sweating is often
accompanied by an unpleasant odor and unpleasant subjective sensations.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
