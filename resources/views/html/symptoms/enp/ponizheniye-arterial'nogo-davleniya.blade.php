<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Emergency medical care
is needed in case of an acute drop in pressure in combination with complaints
such as burning or pressing pain behind the sternum, pallor of the skin, cold
clammy sweat.&nbsp;Chronic and long-term lowering of blood pressure does not
require emergency intervention.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span style='font-size:18.0pt;
