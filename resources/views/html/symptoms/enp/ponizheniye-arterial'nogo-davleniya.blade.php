<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><span lang=EN-US style='font-size:
13.5pt;font-family:"Times New Roman",serif;color:black'>Lowering blood pressure
(arterial hypotension) - a decrease in systolic and diastolic pressure.&nbsp;A
decrease in pressure by 20% of the usual indicators is considered
pathological.&nbsp;It is often accompanied by headache, dizziness, general
weakness, and fainting.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
14.0pt;margin-left:0in;line-height:normal'><b><span lang=EN-US
