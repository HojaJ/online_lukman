short-term loss of consciousness caused by a violation of the blood supply to
the brain with its subsequent spontaneous recovery.&nbsp;Synonyms: syncope,
syncope.</span></p>

<p class=MsoNormal style='margin-top:14.0pt;margin-right:0in;margin-bottom:
